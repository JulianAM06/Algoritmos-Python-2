numero = int(input("Ingresa un numero para saber a que vocal corresponde: "))

if numero == 1: # Si numero es igual a 1
    print("La vocal es la A")

elif numero == 2:
    print("La vocal es la E")

elif numero == 3:
    print("La vocal es la I")

elif numero == 4:
    print("La vocal es la O")

elif numero == 5:
    print("La vocal es la U")

else:
    print("EL numero no corresponde a ninguna vocal")




