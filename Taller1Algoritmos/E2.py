#2.	Se necesita obtener el promedio simple de un estudiante a partir de sus tres notas parciales.

#Leer en Psint es lo mismo que input en Python
#Numero Entero = Int
#Numero Decimal = float
#Caracteres = String
#Boolean = True o False

#Variable de tipo Decimal
nota1 = float(input("Ingresa la primera nota: "))
nota2 = float(input("Ingresa la segunda nota: "))
nota3 = float(input("Ingresa la tercera nota: "))

#sumaNotas = nota1 + nota2 + nota3

#promedio = sumaNotas / 3

#Recomendable
promedio = (nota1 + nota2 + nota3) / 3 

